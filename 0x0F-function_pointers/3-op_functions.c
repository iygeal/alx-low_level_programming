#include "3-cal.h"
/**
 * op_add - Function that returns sum of integers
 * @a: First integer
 * @b: Second integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that returns difference of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that returns product of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that returns the ratio of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Ratio
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that returns the remainder when 2 int divide
 * @a: First integer
 * @b: Second integer
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
