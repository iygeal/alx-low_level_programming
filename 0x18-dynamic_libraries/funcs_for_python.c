#include <stdio.h>

/**
 * add - Function that returns sum of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Sum of the integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function that returns the difference between two integers
 * @a: First integer
 * @b: Second integer
 * Return: Returns the difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function that returns the product of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Returns the product
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function that returns ratio of one integer to another
 * @a: First integer
 * @b: Second integer
 * Return: Ratio
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function that returns the modulus between two integers
 * @a: First integer
 * @b: Second integer
 * Return: Modulus
 */

int mod(int a, int b)
{
	return (a % b);
}
